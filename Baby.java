public class Baby{

	private int babyWeight;
	private int babyColor;
	private int babySize;


	public Baby(int weight, int color, int size){

		babyWeight = weight;
		babyColor = color;
		babySize = size;

	} 

	public int getBabyWeight(){

		return babyWeight;
	}

	public int getBabyColor(){
		
		return babyColor;
	}

	public int getBabySize(){

		return babySize;
	}






}